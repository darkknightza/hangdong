                        <strong><h3>อำนาจหน้าที่ของเทศบาลและการบริหารงานสภาเทศบาล</h3></strong> <br />
                        เพื่อให้เป็นไปตามกฎหมายว่าด้วยข้อมูลข่าวสารของราชการที่จะให้ประชาชนได้มีโอกาสรับรู้ข้อมูลข่าวสารเกี่ยวกับการดำเนินการต่างๆ 
                        ของราชการเพื่อที่ประชาชนจะสามารถแสดงความคิดเห็นและใช้สิทธิทางการเมืองได้โดยถูกต้องกับความเป็นจริง อันเป็นการส่งเสริมให้มีความเป็นรัฐบาลโดยประชาชนมากยิ่งขึ้น 
                        จึงสมควรประกาศโครงสร้างและการจัดหน่วยงาน       อำนาจหน้าที่วิธีการดำเนินงานและสถานที่ติดต่อเพื่อขอรับข้อมูลข่าวสารของเทศบาลตำบล    หางดง  ดังต่อไปนี้<br />

ข้อ 1. เทศบาลตำบลหางดง มีหน้าที่ต้องทำในเขตเทศบาล ดังต่อไปนี้<br />

1.1 รักษาความสงบเรียบร้อยของประชาชน<br />

1.2 ให้มีและบำรุงทางบกและทางน้ำ<br />

1.3 รักษาความสะอาดของถนนหรือทางเดินและที่สาธารณะรวมทั้งการกำจัดมูลฝอยและสิ่งปฏิกูล<br />

1.4 ป้องกันและระงับโรคติดต่อ<br />

1.5 ให้มีเครื่องใช้ในการดับเพลิง<br />

1.6 ให้ราษฎรได้รับการศึกษาอบรม<br />

1.7 ส่งเสริมการพัฒนาสตรี เด็กเยาวชน ผู้สูงอายุและผู้พิการ<br />

1.8 บำรุงศิลปะจารีตประเพณีภูมิปัญญาท้องถิ่นและวัฒนธรรมอันดีงามของท้องถิ่น<br />

1.9 หน้าที่อื่นๆ ตามที่กฎหมายบัญญัติให้เป็นหน้าที่ของเทศบาล<br />
นอกจากนี้เทศบาลตำบลหางดง อาจจัดทำกิจการใดๆ ในเขตเทศบาลตำบลหางดง ภายใต้บังคับแห่งกฎหมาย ดังนี้<br />

(1)  ให้มีน้ำสะอาดหรือการประปา<br />

(2)  ให้มีโรงฆ่าสัตว์<br />

(3)  ให้มีตลาด ท่าเทียบเรือและท่าข้าม<br />

(4)  ให้มีสุสานและฌาปนสถาน<br />

(5)  บำรุงและส่งเสริมการทำมาหากินของราษฎร
<br />
(6)  ให้มีและบำรุงสถานที่ทำการพิทักษ์รักษาคนเจ็บไข้<br />

(7)  ให้มีและบำรุงการไฟฟ้าหรือแสงสว่างโดยวิธีอื่น<br />

(8)  ให้มีและบำรุงทางระบายน้ำ<br />

(9)  เทศพาณิชย์
