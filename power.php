ข้อ 2. เทศบาลตำบลหางดง มีโครงสร้างการบริหารงาน ดังต่อไปนี้<br />

2.1 สภาเทศบาลตำบลหางดง ประกอบด้วย ประธานสภาเทศบาล รองประธานสภาเทศบาล เลขานุการสภาเทศบาล และสมาชิกสภาเทศบาล<br />

2.2 ผู้บริหารท้องถิ่น ประกอบด้วย นายกเทศมนตรี กับรองนายกเทศมนตรีอื่นอีกสองคน<br />

2.3 ผู้ช่วยผู้บริหารท้องถิ่น ประกอบด้วย เลขานุการนายกเทศมนตรีและที่ปรึกษานายกเทศมนตรี<br />

