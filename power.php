ข้อ 3. อำนาจหน้าที่ที่สำคัญของสภาเทศบาลตำบลหางดง และผู้บริหารท้องถิ่น<br />

         โดยสรุป ดังต่อไปนี้<br />

3.1 อำนาจหน้าที่ของสภาเทศบาล<br />

3.1.1 พิจารณาและให้ความเห็นชอบร่างเทศบัญญัติ
<br />
3.1.2 ควบคุมการปฏิบัติงานของผู้บริหารท้องถิ่น ให้เป็นไปตามนโยบายและแผน พัฒนาเทศบาล
<br />
3.1.3 ควบคุมการปฏิบัติงานของผู้บริหารท้องถิ่น ให้เป็นไปตามระเบียบกฎหมาย
<br />
3.2 อำนาจหน้าที่ของผู้บริหารท้องถิ่น<br />

3.2.1 รับผิดชอบงานนโยบายหรืองานอื่นใดที่กฎหมาย ระเบียบ ข้อบังคับ หรือคำสั่งระบุไว้ให้เป็นหน้าที่ของผู้บริหารท้องถิ่นโดยเฉพาะ<br />

3.2.2 ในการปฏิบัติงานของเทศบาลเกี่ยวกับการวินิจฉัยปัญหาและสั่งการให้ใช้หลักการแบ่งอำนาจและหน้าที่ ตามลักษณะของเรื่อง ดังต่อไปนี้<br />

ก. นายกเทศมนตรี
<br />
(1) ในเรื่องเกี่ยวกับสภาเทศบาล หรือสมาชิกสภาเทศบาลตามกฎหมายว่าด้วยการเทศบาล<br />

(2) เรื่องที่จะต้องรายงานต่ออำเภอ หรือจังหวัด หรือกระทรวงมหาดไทย แล้วแต่กรณี<br />

(3) เรื่องที่กฎหมายหรือระเบียบข้อบังคับระบุไว้ให้เป็นอำนาจหน้าที่ของผู้บริหารท้องถิ่น<br />

(4) เรื่องเกี่ยวกับการออกเทศบัญญัติ หรือยกเลิกแก้ไขเพิ่มเติม เทศบัญญัติที่มีอยู่แล้ว<br />

(5) เรื่องที่ดำริขึ้นใหม่ซึ่งอาจจะต้องมีโครงการหรือแผนการหรือระเบียบการขึ้นใหม่<br />

(6) เรื่องซึ่งปลัดเทศบาลเห็นเป็นปัญหาหรือเป็นกรณีพิเศษที่ควรได้รับการวินิจฉัยจากนายกเทศมนตรี<br />

(7)  เรื่องซึ่งนายกเทศมนตรีมีอำนาจสั่งการโดยเฉพาะ
<br />
(8)  เรื่องที่ปลัดเทศบาลเห็นสมควรเสนอเพื่อทราบ
<br />
ข. รองนายกเทศมนตรี<br />

(1) เรื่องที่รองนายกเทศมนตรีหรือนายกเทศมนตรี มอบหมายให้เป็นผู้รับผิดชอบในงานประเภทใดโดยเฉพาะ<br />

(2)  เรื่องที่จะต้องรายงานต่อนายกเทศมนตรี<br />

(3)  เรื่องที่กฎหมาย หรือระเบียบข้อบังคับระบุไว้ให้เป็นอำนาจหน้าที่ของรองนายกเทศมนตรี<br />

ข้อ 4. การบริหารกิจการของเทศบาล ให้ทำในนามของนายกเทศมนตรี ถ้านายกเทศมนตรี    ไม่อาจบริหารกิจการได้ ให้นายกเทศมนตรีทำคำสั่งตั้งรองนายกเทศมนตรีให้ทำการแทน<br />

เทศบาลตำบลหางดง มีพนักงานเทศบาล โดยมีปลัดเทศบาลเป็นผู้บังคับบัญชาพนักงานประจำ ตลอดจนคนงานหรือลูกจ้าง และรับผิดชอบในงานประจำทั่วไปของเทศบาลโดยแบ่งการบริหารงานเป็นส่วนราชการ ดังต่อไปนี้<br />

